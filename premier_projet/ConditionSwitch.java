package premier_projet;

public class ConditionSwitch {

	public static void main(String[] args) {

		int note = 20;
		switch (note) {
		case 10:
			System.out.println("vous avez eu 10");
			break;
		case 15:
			System.out.println("15");
			break;
		case 14:
			System.out.println("14");
			break;
		default:
			System.out.println("aucune des valeurs");

		}

	}

}
